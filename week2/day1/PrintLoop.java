package week2.day1;

public class PrintLoop {

	public static void main(String[] args) {
		
		// Whenever you are repeating same code multiple time -> end with loop
		// for, while
		// for -> starting point and ending point 
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		

	}

}
